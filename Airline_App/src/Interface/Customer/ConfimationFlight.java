    private void btnBookTicketActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBookTicketActionPerformed
        // TODO add your handling code here:
        customer = new Customer();
           try{ 
            if (txtCustName.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null,"Please enter customer name!!");
                return;
            } else {
                customer.setCustomerName(txtCustName.getText());
            }
            
                 if (txtAreaAddress.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null,"Please enter Address!!");
                return;
            } else {
                 customer.setAddr(txtAreaAddress.getText());
            }
                 
                  if (!datePatternCorrect())
        {
            JOptionPane.showMessageDialog(null, "Date should be in the form : MM/DD/YYYY");
             return;
        }
            
            
            customer.setContactNo(Integer.parseInt(txtPhoneNumber.getText()));
           
           
           
            int key = seatSelect.getSelectedIndex();
            String value = seatSelect.getSelectedItem().toString();
            flight.getSeatalloted().populateHashMap(key, value);
            
            
            String selected = seatSelect.getSelectedItem().toString();
            customer.setSeatNo(selected);
     
            //String value = seatalloted.getSelectedItem().toString();
             //customer.setSeatNo(value);
            
            //JOptionPane.showMessageDialog(null, "Your Ticket has been booked");
            
            AskConfirmation askconfirm = new AskConfirmation(rightJPanel, flight, customer);
            rightJPanel.add("AskConfirmation", askconfirm);
            CardLayout layout = (CardLayout) rightJPanel.getLayout();
            layout.next(rightJPanel);
            }catch (NumberFormatException e) {
            
            JOptionPane.showMessageDialog(null, "Please enter number values for Phone Number!!", "Warning", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnBookTicketActionPerformed

