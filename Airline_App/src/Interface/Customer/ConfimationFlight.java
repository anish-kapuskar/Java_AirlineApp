   private boolean datePatternCorrect(){
            Pattern p = Pattern.compile("^[0-9]{4}+/[0-9]{2}+/[0-9]{2}+$");
    Matcher m = p.matcher(txtDate.getText());
    boolean b = m.matches();
    return b;
     }

